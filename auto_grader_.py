from openai import OpenAI

def get_grade(input):
    client = OpenAI(api_key = openai.api_key)  # Initialize the client

    raw_grade = client.chat.completions.create(
        messages=input,
        model=MODEL_NAME,
        temperature=0,
        max_tokens=500,
        top_p=1,
        frequency_penalty=0,
        presence_penalty=0
    )

    return raw_grade.choices[0].message.content

